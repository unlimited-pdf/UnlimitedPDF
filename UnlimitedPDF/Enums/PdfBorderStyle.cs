namespace UnlimitedPDF.Enums;

/// <summary>
/// Enum represents different styles of borders that can be applied to PDF elements.
/// </summary>
public enum PdfBorderStyle
{
    /// <summary>
    /// No border is applied.
    /// </summary>
    None,

    /// <summary>
    /// Border with solid lines is applied.
    /// </summary>
    Solid,

    /// <summary>
    /// Border with dashed lines is applied.
    /// </summary>
    Dashed,

    /// <summary>
    /// Border with dotted lines is applied.
    /// </summary>
    Double
}
