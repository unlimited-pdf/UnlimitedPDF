namespace UnlimitedPDF.Enums;

/// <summary>
/// Enum represents vertical alignment options for content within a PDF cell.
/// </summary>
public enum PdfCellVerticalAlignment 
{
    /// <summary>
    /// Aligns content to the top of the cell.
    /// </summary>
    Top,

    /// <summary>
    /// Aligns content to the middle of the cell.
    /// </summary>
    Middle,

    /// <summary>
    /// Aligns content to the bottom of the cell.
    /// </summary>
    Bottom
}
